/** Overlay type accessory pixels onto a base frame (non-destructive copy). */
function paint(base: PetFrame, stamps: Array<[number, number, number]>): PetFrame {
  const out = base.map((row) => row.slice());
  for (const [x, y, v] of stamps) {
    if (y >= 0 && y < out.length && x >= 0 && x < out[0].length) out[y][x] = v;
  }
  return out;
}

