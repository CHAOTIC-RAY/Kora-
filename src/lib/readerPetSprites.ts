function pack(
  idleA: PetFrame,
  idleB: PetFrame,
  happy: PetFrame,
  sleepA: PetFrame,
  sleepB: PetFrame,
  sad: PetFrame,
  scared: PetFrame,
  angry: PetFrame,
  love: PetFrame,
  curious: PetFrame,
  page: PetFrame
): Record<PetMood, PetFrame[]> {
  return {
    idle: [idleA, idleB],
    happy: [happy, idleA],
    sleepy: [sleepA, sleepB],
    sad: [sad, idleA],
    scared: [scared, idleA],
    angry: [angry, idleA],
    love: [love, happy],
    curious: [curious, idleA],
    pageTurn: [page, happy, idleA],
  };
}

